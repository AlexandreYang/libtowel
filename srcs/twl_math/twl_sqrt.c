double				twl_sqrt(const double m)
{
	double			i;
	int				j;
	double			x1;
	double			x2;

	i = 0;
	while ((i * i) <= m)
		i += 0.1f;
	x1 = i;
	j = 0;
	while (j < 10)
	{
		x2 = m;
		x2 /= x1;
		x2 += x1;
		x2 /= 2;
		x1 = x2;
		j++;
	}
	return (x2);
}
