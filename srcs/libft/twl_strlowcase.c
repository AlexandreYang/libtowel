char		*twl_strlowcase(char *str)
{
	int i;

	if (!str)
		return (void *)0;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] = (char)(str[i] + 32);
		}
		i++;
	}
	return (str);
}
